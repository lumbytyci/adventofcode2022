    public static int RunPart(string input)
    {
        var clock = 0;
        var state = new List<int> { 1 };
        var samples = new Stack<int>(new int[] { 20, 60, 100, 140, 180, 220 }.Reverse());
        var signalStrength = 0;

        foreach (var line in input.Split(Environment.NewLine))
        {
            var isNoop = false;
            if (line.Length > 4)
            {
                clock += 2;
                var operand = int.Parse(line.Substring(line.LastIndexOf(" ")));
                state.Add(state.Last() + operand);
            }
            else
            {
                clock++;
                isNoop = true;
            }

            if (samples.Any() && samples.Peek() <= clock)
            {
                signalStrength += samples.Pop() * (!isNoop ? state[^2] : state.Last());
            }
        }

        return signalStrength;
    }
